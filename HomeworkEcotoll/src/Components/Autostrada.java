package Components;

public class Autostrada {
		public Integer IDAutostrada;
		public String nome;
		public String tipo;

public Autostrada(Integer IDAutostrada,String nome,String tipo) {
		this.IDAutostrada=IDAutostrada;
		this.nome=nome;
		this.tipo=tipo;
	}
		public Integer getIDAutostrada() {
				return IDAutostrada;
}
		public void setIDAutostrada(Integer IDAutostrada) {
				this.IDAutostrada= IDAutostrada;
}
		public String getNome() {
				return nome;
}
		public void setNome(String nome) {
				this.nome= nome;
}
		public String getTipo() {
				return tipo;
}
		public void setTipo(String tipo) {
				this.tipo= tipo;
}




}
